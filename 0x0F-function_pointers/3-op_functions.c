#include "3-calc.h"
#include <stdlib.h>

/**
 * op_add - Return the sum
 * @a: first parameter
 * @b: second parameter
 *
 * Return: sum
 *
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Return the sub
 * @a: first parameter
 * @b: second parameter
 *
 * Return: sub
 *
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Return the mul
 * @a: first parameter
 * @b: second parameter
 *
 * Return: mul
 *
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Return the div
 * @a: first parameter
 * @b: second parameter
 *
 * Return: div
 *
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Return the mod
 * @a: first parameter
 * @b: second parameter
 *
 * Return: mod
 *
 */
int op_mod(int a, int b)
{
	return (a % b);
}
